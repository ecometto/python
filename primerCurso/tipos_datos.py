# tipo de datos
numero = 15
print (numero, type(numero))

# tipo de datos flotantes
num_flotante = 15.5
print(num_flotante, type(num_flotante))


# tipo de datos complejo
num_complejo = 15 + 6j
print(num_complejo, type(num_complejo))

# tipo string str
palabra = "palabra1"

print (palabra)


# tipo boolean (verdadero o falso)

numero = 3==4

print (numero, type(numero))
